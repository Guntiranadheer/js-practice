// when we create with variable  with var keyword
//var keyword contains the global scope i,e means we can access anywere ex:
var name="apple";
console.log(name);
name="mango";
console.log(name);
// when we create variable with let keyword
// let keyword contains the block scope or function scope i.e means we can access inside the block or function " we cannot access outside of function or outside of block"
//ex:
let food="chicken biryani";
food="fish fry";
console.log(food);
// when we create variable  with const keyword
// when we assign value we cannot change
const cartoonSeries="chota-beem";
console.log(cartoonSeries);
